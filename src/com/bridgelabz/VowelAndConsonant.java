package com.bridgelabz;

public class VowelAndConsonant {
    public static void main(String[] args) {

        String k="A";
        switch (k){
            case "a":
            case "e":
            case "i":
            case "o":
            case "u":
            case "A":
            case "E":
            case "I":
            case "O":
            case "U":
                System.out.println("Vowel");
                break;
            default:
                System.out.println("consonent");
        }

    }
}
